def check_image_size(img):
    """Check 1: Dimensions ≤ 1280px."""
    w, h = img.size
    ok = w <= 1280 and h <= 1280
    return {
        "check": "image_size",
        "grade": "PASS" if ok else "FAIL",
        "detail": f"{w}×{h} px" + ("" if ok else " — EXCEEDS 1280px hard limit"),
    }


def check_not_solid(img):
    """Check 6: Image is not a solid single color."""
    w, h = img.size
    # Sample 200 pixels from corners and center
    sample_pts = [
        (0, 0), (w-1, 0), (0, h-1), (w-1, h-1), (w//2, h//2),
        (w//4, h//4), (3*w//4, h//4), (w//4, 3*h//4), (3*w//4, 3*h//4),
        (w//3, h//2), (2*w//3, h//2),
    ]
    px = img.load()
    unique = set()
    for (x, y) in sample_pts:
        xc = min(x, w - 1)
        yc = min(y, h - 1)
        unique.add(px[xc, yc])
    ok = len(unique) >= 3
    return {
        "check": "not_solid",
        "grade": "PASS" if ok else "FAIL",
        "detail": f"{len(unique)} unique colors in sample" + ("" if ok else " — image may be blank"),
    }


def check_panel_count(img, expected_panels, panels):
    """Check 2: Detected panel count matches expected."""
    found = len(panels)
    ok = found == expected_panels
    grade = "PASS" if ok else "WARN"
    return {
        "check": "panel_count",
        "grade": grade,
        "detail": f"Expected {expected_panels} panels, detected {found}",
    }


def check_annotation_occupancy(img, panels, threshold=OCCUPANCY_WARN_THRESHOLD,
                                annot_y_start=ANNOT_ZONE_ABS_Y_START,
                                annot_y_end=ANNOT_ZONE_ABS_Y_END,
                                annot_bg_spec=None,
                                dark_sheet=False,
                                occupancy_threshold_dark=None):
    """
    Check 3: Each panel's annotation zone has sufficient non-BG pixel density.

    C40: annot_y_start / annot_y_end now supplied by caller from sheet geometry
    config, eliminating false WARNs caused by hard-coded zone coords that did
    not match sheets with different HEADER_H values.

    C43: annot_bg_spec=(bg_rgb, tolerance) from per-family config. When present,
    uses precise background color matching instead of the broad (200-255) light
    range. This prevents light character fill areas (Luma face fill, Cosmo skin
    highlights, cardigan RW-08) from being mis-classified as panel background,
    which caused false annotation_occupancy WARNs on light-bg sheets.
    The bg_mode label in the detail string shows which path was used.

    C46: dark_sheet=True enables bright-pixel counting mode for void-black sheets
    (Byte, Glitch). occupancy_threshold_dark overrides `threshold` when dark_sheet
    is True. bg_mode label shows "dark-bright" to distinguish this path.
    """
    w, h = img.size
    results = []
    any_warn = False

    if dark_sheet:
        bg_mode = "dark-bright"
        effective_threshold = occupancy_threshold_dark if occupancy_threshold_dark is not None else threshold
    else:
        bg_mode = "precise" if annot_bg_spec is not None else "legacy-broad"
        effective_threshold = threshold

    for i, (x0, x1) in enumerate(panels):
        y0 = annot_y_start
        y1 = min(annot_y_end, h - LABEL_ZONE_H - 2)
        if y1 <= y0:
            continue
        crop = img.crop((x0, y0, x1, y1))
        non_bg, total = _count_non_bg(crop, annot_bg_spec=annot_bg_spec, dark_sheet=dark_sheet)
        occ = non_bg / max(total, 1)
        ok = occ >= effective_threshold
        if not ok:
            any_warn = True
        results.append(f"P{i+1}: {occ:.1%} occupancy" + ("" if ok else " — WARN low"))

    detail = f"[bg:{bg_mode}]  " + ("  |  ".join(results) if results else "no panels found")
    return {
        "check": "annotation_occupancy",
        "grade": "WARN" if any_warn else "PASS",
        "detail": detail,
    }


def check_beat_badge_zones(img, panels, badge_panel_top_abs=56):
    """
    Check 4: Each panel should have a beat-badge (colored block, top-left corner).
    Looks for non-BG pixels in the badge area.

    C40: badge_panel_top_abs now supplied by caller from sheet geometry config.
    Previously hard-coded to 56, which didn't match sheets with HEADER_H != 44.
    """
    w, h = img.size
    results = []
    any_warn = False

    for i, (x0, x1) in enumerate(panels):
        bx0 = x0 + BADGE_PAD
        bx1 = min(x0 + BADGE_PAD + BADGE_W, x1)
        by0 = badge_panel_top_abs + BADGE_PAD
        by1 = badge_panel_top_abs + BADGE_PAD + BADGE_H
        if bx1 <= bx0 or by1 >= h:
            results.append(f"P{i+1}: skip (out of bounds)")
            continue
        crop = img.crop((bx0, by0, bx1, by1))
        non_bg, total = _count_non_bg(crop)
        occ = non_bg / max(total, 1)
        ok = occ >= 0.15   # badge should be at least 15% colored
        if not ok:
            any_warn = True
        results.append(f"P{i+1}: {occ:.1%}" + ("" if ok else " — WARN no badge"))

    return {
        "check": "beat_badges",
        "grade": "WARN" if any_warn else "PASS",
        "detail": "beat badge occupancy  " + "  |  ".join(results),
    }


def _count_beat_color(img_crop, beat_color_range=None):
    """
    Count BEAT_COLOR pixels in a cropped PIL image.

    C41: If beat_color_range is supplied (from per-family config), use it.
    Falls back to legacy BEAT_CYAN_MIN/BEAT_CYAN_MAX range otherwise.

    beat_color_range: (min_rgb, max_rgb) tuple or None.
    """
    if beat_color_range is None:
        # Legacy fallback: cyan family
        return _count_cyan(img_crop)
    lo, hi = beat_color_range
    w, h = img_crop.size
    count = 0
    px = img_crop.load()
    for y in range(h):
        for x in range(w):
            if _pixel_in_range(px[x, y], lo, hi):
                count += 1
    return count, w * h


def check_timing_colors(img, panels,
                        annot_y_start=ANNOT_ZONE_ABS_Y_START,
                        annot_y_end=ANNOT_ZONE_ABS_Y_END,
                        beat_color_range=None):
    """
    Check 5: BEAT_COLOR pixels should appear in annotation zone of ≥50% of panels.

    C40: annot_y_start / annot_y_end now supplied by caller from sheet geometry
    config (same as check_annotation_occupancy).

    C41: beat_color_range (min_rgb, max_rgb) from per-family config replaces the
    hard-coded BEAT_CYAN range. Luma uses blue (80,120,200); Byte uses cyan
    (0,190,215). None → falls back to legacy cyan-family range.
    """
    w, h = img.size
    panels_with_beat = 0
    color_label = "config" if beat_color_range is not None else "legacy-cyan"

    for x0, x1 in panels:
        y0 = annot_y_start
        y1 = min(annot_y_end, h - LABEL_ZONE_H - 2)
        if y1 <= y0:
            continue
        crop = img.crop((x0, y0, x1, y1))
        beat_count, total = _count_beat_color(crop, beat_color_range)
        if beat_count / max(total, 1) >= 0.005:   # 0.5% of zone is beat color
            panels_with_beat += 1

    n = len(panels)
    frac = panels_with_beat / max(n, 1)
    ok = frac >= 0.50
    return {
        "check": "timing_colors",
        "grade": "PASS" if ok else "WARN",
        "detail": (
            f"BEAT_COLOR [{color_label}] in {panels_with_beat}/{n} panels ({frac:.0%})"
        ),
    }


