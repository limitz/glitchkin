def lint_motion_spec(path: str, expected_panels: int = None,
                     occupancy_threshold: float = OCCUPANCY_WARN_THRESHOLD,
                     geo_config_path: str = None) -> dict:
    """
    Lint a motion spec sheet PNG.

    Args:
        path: Path to the PNG file.
        expected_panels: Expected number of panels. If None, auto-detect from
                         sheet_geometry_config.json or filename heuristic.
        occupancy_threshold: Minimum annotation occupancy fraction (default 0.04).
        geo_config_path: Path to sheet_geometry_config.json. Defaults to
                         output/tools/sheet_geometry_config.json (auto-resolved).

    Returns:
        dict with keys:
            file, width, height, overall (PASS/WARN/FAIL), checks (list of dicts),
            pass, warn, fail
    """
    result = {
        "file": str(path),
        "width": 0,
        "height": 0,
        "overall": "ERROR",
        "checks": [],
        "pass": 0,
        "warn": 0,
        "fail": 0,
        "flagged": [],
        "missing": [],
    }

    if not os.path.exists(path):
        result["overall"] = "ERROR"
        result["missing"].append(str(path))
        result["checks"].append({
            "check": "file_exists",
            "grade": "FAIL",
            "detail": f"File not found: {path}",
        })
        result["fail"] = 1
        return result

    try:
        img = _load_image(path)
    except Exception as e:
        result["overall"] = "ERROR"
        result["checks"].append({
            "check": "file_load",
            "grade": "FAIL",
            "detail": f"Cannot open image: {e}",
        })
        result["fail"] = 1
        return result

    result["width"] = img.width
    result["height"] = img.height

    # --- C40/C41/C43/C46: Load sheet geometry config ---
    geo_config = _load_geo_config(geo_config_path)
    fname = os.path.basename(path)
    (annot_y_start, annot_y_end, badge_panel_top, config_panels,
     beat_color_range, annot_bg_spec, dark_sheet, occupancy_threshold_dark) = \
        _get_zone_params(fname, geo_config)
    geo_source = "config" if geo_config and _family_from_filename(fname) else "legacy defaults"

    # Determine expected panel count
    # Priority: caller arg > config > filename heuristic
    if expected_panels is None:
        if config_panels is not None:
            expected_panels = config_panels
        else:
            fname_lower = fname.lower()
            if "luma" in fname_lower:
                expected_panels = 4  # Luma sheet has 4 panels
            elif "byte" in fname_lower:
                expected_panels = 4  # Byte COMMITMENT arc is 4 panels
            else:
                expected_panels = 3  # conservative default

    # --- Run checks ---
    checks = []

    # C1: image size
    checks.append(check_image_size(img))

    # C6: not solid
    checks.append(check_not_solid(img))

    # Detect panels
    panels = _detect_panels(img, expected_panels)

    # C2: panel count
    checks.append(check_panel_count(img, expected_panels, panels))

    # C3: annotation occupancy (C40: calibrated zone coords; C43: precise bg color;
    #     C46: dark-sheet bright-pixel mode for Byte + Glitch)
    checks.append(check_annotation_occupancy(img, panels,
                                              threshold=occupancy_threshold,
                                              annot_y_start=annot_y_start,
                                              annot_y_end=annot_y_end,
                                              annot_bg_spec=annot_bg_spec,
                                              dark_sheet=dark_sheet,
                                              occupancy_threshold_dark=occupancy_threshold_dark))

    # C4: beat badge zones (C40: uses calibrated badge_panel_top_abs)
    checks.append(check_beat_badge_zones(img, panels,
                                          badge_panel_top_abs=badge_panel_top))

    # C5: timing colors (C40: calibrated zone coords; C41: per-family beat color)
    checks.append(check_timing_colors(img, panels,
                                       annot_y_start=annot_y_start,
                                       annot_y_end=annot_y_end,
                                       beat_color_range=beat_color_range))

    # Record geometry source for transparency
    result["geo_source"] = geo_source

    # --- Tally ---
    grade_order = {"FAIL": 2, "WARN": 1, "PASS": 0, "ERROR": 3}
    worst = "PASS"
    for c in checks:
        g = c["grade"]
        if grade_order.get(g, 0) > grade_order.get(worst, 0):
            worst = g
        if g == "PASS":
            result["pass"] += 1
        elif g == "WARN":
            result["warn"] += 1
            result["flagged"].append(f"WARN: {c['check']}: {c['detail']}")
        elif g in ("FAIL", "ERROR"):
            result["fail"] += 1
            result["flagged"].append(f"FAIL: {c['check']}: {c['detail']}")

    result["overall"] = worst
    result["checks"] = checks
    return result


# ---------------------------------------------------------------------------
# Format report
# ---------------------------------------------------------------------------

def format_report(results: list) -> str:
    """
    Format a list of lint_motion_spec() results as a readable text block.
    """
    lines = ["## Motion Spec Lint — LTG_TOOL_motion_spec_lint", ""]
    total_pass = total_warn = total_fail = 0

    for r in results:
        fname = os.path.basename(r["file"])
        overall = r["overall"]
        size_str = f"{r['width']}×{r['height']}" if r["width"] else "N/A"
        geo_src = r.get("geo_source", "")
        geo_tag = f"  geo:{geo_src}" if geo_src else ""
        lines.append(f"### {fname}  [{overall}]  ({size_str}){geo_tag}")
        for c in r["checks"]:
            icon = {"PASS": "✓", "WARN": "⚠", "FAIL": "✗", "ERROR": "✗"}.get(c["grade"], "?")
            lines.append(f"  {icon} [{c['grade']:4s}] {c['check']}: {c['detail']}")
        lines.append("")
        total_pass += r["pass"]
        total_warn += r["warn"]
        total_fail += r["fail"]

    lines.append(f"**Totals — PASS: {total_pass}  WARN: {total_warn}  FAIL: {total_fail}**")
    return "\n".join(lines)


