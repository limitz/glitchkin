def _load_image(path: str):
    """Load image and return PIL Image, or raise."""
    if not HAS_PIL:
        raise ImportError("Pillow (PIL) is required for motion spec lint.")
    return Image.open(path).convert("RGB")


def _pixel_in_range(pixel, lo, hi):
    """Check if RGB pixel is within lo/hi bounds (inclusive)."""
    return all(lo[i] <= pixel[i] <= hi[i] for i in range(3))


def _is_bg_pixel(pixel):
    """True if pixel looks like annotation panel background (light gray/white)."""
    return _pixel_in_range(pixel, ANNOT_BG_MIN, ANNOT_BG_MAX)


def _is_dark_bg_pixel(pixel):
    """True if pixel is dark background (void/commitment panel)."""
    return all(pixel[i] <= DARK_BG_MAX[i] for i in range(3))


def _is_cyan_pixel(pixel):
    """True if pixel is in the BEAT_COLOR cyan family."""
    return _pixel_in_range(pixel, BEAT_CYAN_MIN, BEAT_CYAN_MAX)


def _is_any_bg(pixel):
    return _is_bg_pixel(pixel) or _is_dark_bg_pixel(pixel)


def _is_precise_bg_pixel(pixel, bg_rgb, tolerance):
    """
    True if pixel is within `tolerance` of `bg_rgb` in all three channels.

    C43: Used when per-family annotation_bg_color is configured. More selective
    than the broad (200-255) range — excludes light character fill areas that
    have the same lightness as the panel background but a different hue/value.
    """
    return all(abs(pixel[i] - bg_rgb[i]) <= tolerance for i in range(3))


def _count_non_bg(img_crop, annot_bg_spec=None, dark_sheet=False):
    """
    Count non-background pixels in a cropped PIL image.

    C43: When annot_bg_spec=(bg_rgb, tolerance) is supplied, uses precise
    per-family background matching (_is_precise_bg_pixel) instead of the broad
    light-range heuristic (_is_bg_pixel). The dark-BG check (_is_dark_bg_pixel)
    is always applied regardless of mode, so void/commitment panels still work.

    C46: When dark_sheet=True (Byte, Glitch), inverts the counting logic:
    counts pixels whose per-channel mean exceeds 40 as annotation content
    rather than subtracting background. This is appropriate for void-black
    panels where annotation text/arrows are bright-on-dark and the traditional
    background-subtraction approach under-counts content. The result should be
    compared against a lower threshold (occupancy_threshold_dark ≈ 0.010)
    rather than the default 0.04. annot_bg_spec is ignored in dark-sheet mode.
    """
    w, h = img_crop.size
    count = 0
    px = img_crop.load()
    if dark_sheet:
        # C46: count bright pixels as annotation content on void-black sheets
        for y in range(h):
            for x in range(w):
                p = px[x, y]
                if (p[0] + p[1] + p[2]) // 3 > 40:
                    count += 1
    elif annot_bg_spec is not None:
        bg_rgb, tol = annot_bg_spec
        for y in range(h):
            for x in range(w):
                p = px[x, y]
                if not (_is_precise_bg_pixel(p, bg_rgb, tol) or _is_dark_bg_pixel(p)):
                    count += 1
    else:
        for y in range(h):
            for x in range(w):
                if not _is_any_bg(px[x, y]):
                    count += 1
    return count, w * h


def _count_cyan(img_crop):
    """Count BEAT_COLOR cyan-family pixels in a cropped PIL image."""
    w, h = img_crop.size
    count = 0
    px = img_crop.load()
    for y in range(h):
        for x in range(w):
            if _is_cyan_pixel(px[x, y]):
                count += 1
    return count, w * h


def _detect_panels(img, expected_cols):
    """
    Detect panel columns by looking for vertical dark-gap columns.
    Returns list of (x_start, x_end) tuples for each panel column.

    Falls back to even division if detection fails.
    """
    w, h = img.size
    px = img.load()
    mid_y = h // 2

    # Build column darkness profile using middle row of image
    col_dark = []
    for x in range(w):
        total = sum(px[x, mid_y])
        col_dark.append(total < 3 * PANEL_EDGE_DARK_THRESHOLD)

    # Find gap columns (dark bands)
    gaps = []
    in_gap = False
    gap_start = 0
    for x, is_dark in enumerate(col_dark):
        if is_dark and not in_gap:
            in_gap = True
            gap_start = x
        elif not is_dark and in_gap:
            in_gap = False
            gap_w = x - gap_start
            if gap_w >= 2:
                gaps.append((gap_start, x - 1))

    # Build panel regions from gaps
    regions = []
    prev_end = 0
    for gx0, gx1 in gaps:
        if gx0 > prev_end + 10:
            regions.append((prev_end, gx0 - 1))
        prev_end = gx1 + 1
    if prev_end < w - 10:
        regions.append((prev_end, w - 1))

    # Filter to non-trivial regions
    min_panel_w = w // (expected_cols * 2)
    regions = [(x0, x1) for x0, x1 in regions if (x1 - x0) >= min_panel_w]

    if len(regions) == expected_cols:
        return regions

    # Fallback: even division
    panel_w = w // expected_cols
    return [(i * panel_w, (i + 1) * panel_w - 1) for i in range(expected_cols)]


