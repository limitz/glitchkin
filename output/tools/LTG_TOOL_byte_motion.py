def _load_header_h_byte(default=44):
    """Load HEADER_H for the byte family from sheet_geometry_config.json."""
    try:
        import json
        with open(_CONFIG_PATH) as f:
            cfg = json.load(f)
        geo = cfg.get("families", {}).get("byte", {})
        # For byte: header_h == panel_top_abs - PAD (PAD=12 for byte)
        # We store panel_top_abs; HEADER_H = panel_top_abs - PAD
        panel_top = geo.get("panel_top_abs", None)
        if panel_top is not None:
            return max(panel_top - 12, default)  # PAD=12 for byte
        return geo.get("header_h", default)
    except Exception:
        return default

